public class Calculadora {
    
    
    public Integer opercaoAdicao(Integer num1,Integer num2){
        return (num1 + num2);
    }
    public Integer opercaoSubtracao(Integer num1, Integer num2){
        return (num1 - num2);
    }
    public Integer opercaoMultiplicacao(Integer num1, Integer num2){
        return  (num1 * num2);
    }
    public Integer opercaoDivisao(Integer num1, Integer num2){
        return (num1 / num2);
    }

 
    
}
